package day4.LaboratoryExercise;

public class if_Cortes {
    public static void main(String[] args) {
        int iNumber = 3;
        
        if (iNumber == 0) {
            System.out.println("first string");
        }
        
        if (iNumber >= 0) {
            System.out.println("second string");
        }
        
        else {
            System.out.println("third string");
        }
    }
}
